package br.com.projetoMVC.model;

public class Produto {

	int id;
	String descricao;
	
	
	
	
	public Produto() {
		super();
	}
	

	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getDescricao() {
		return descricao;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	
	
	
}
